#Parametros da Função

def matematicaSimples(num1, num2): #dentro de () ele funcionara como una variavel
	resposta = num1 + num2
	print ('num1 é :', num1)
	print ('num2 é :', num2)
	print ('A soma deles é :', resposta)

matematicaSimples(5,3) #quando chamar a função você ira colocar dentro de () o valor das variaveis em ordem


